def build_places_prompt(query: str, city_name: str, topic: str) -> str:
    return (
        f"Город: {city_name}\n"
        f"Тема: {topic}\n"
        f"Запрос пользователя:\n{query}\n\n"
        "Подбери места, куда можно сходить по этому запросу."
    )


def build_user_prompt(
    query: str,
    candidates_json: str,
    *,
    constraints_note: str | None = None,
) -> str:
    constraint_block = ""
    if constraints_note:
        constraint_block = f"\nОграничения запроса:\n{constraints_note}\n"
    return (
        "Запрос пользователя:\n"
        f"{query}\n"
        f"{constraint_block}\n"
        "Кандидаты (JSON):\n"
        f"{candidates_json}\n\n"
        "Выбери до 10 наиболее релевантных event_ids. "
        "Сначала учитывай дату/день недели из запроса, затем тему. "
        "Если прямого совпадения нет — предложи близкие по теме события."
    )
