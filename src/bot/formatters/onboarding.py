from __future__ import annotations

CITY_LABELS: dict[str, str] = {
    "moscow": "Москва",
    "spb": "Санкт-Петербург",
}


def city_label(city_slug: str) -> str:
    return CITY_LABELS.get(city_slug, city_slug)


def welcome_city_prompt() -> str:
    return (
        "Здравствуйте!\n\n"
        "Я помогу подобрать досуг в вашем городе — мероприятия и места на ближайшие 30 дней.\n\n"
        "Для начала выберите город."
    )


def first_launch_guide(city_slug: str) -> str:
    city = city_label(city_slug)
    return (
        f"Добро пожаловать!\n"
        f"Город: **{city}**\n\n"
        "**Как работает бот**\n\n"
        "**1. Стандартные сценарии**\n"
        "Используйте кнопки меню или пройдите опрос по шагам — бот подберёт варианты "
        "по заданным параметрам (аудитория, категория, бюджет, формат).\n\n"
        "• 🎲 **Случайный вариант** — одна карточка из вашего города\n"
        "• 📝 **Опрос** — пошаговый подбор с персональной карточкой\n"
        "• 🔥 **Популярное** — топ событий на ближайшие 30 дней\n"
        "• ❤️ **Избранное** — сохранённые карточки\n"
        "• ⚙️ **Настройки** — смена города\n\n"
        "**2. ИИ-ассистент**\n"
        "Если ваш запрос **не укладывается** в кнопки меню или шаги опроса — "
        "напишите сообщение **обычным текстом**. Подключится ИИ-ассистент: "
        "он поможет найти мероприятия или места по вашей формулировке.\n\n"
        "_Примеры:_ «джаз в эти выходные», «куда сходить на бильярд».\n\n"
        "**Важно:** на шагах опроса ответ даётся только кнопками. "
        "Текстовое сообщение завершит опрос и передаст запрос ИИ-ассистенту.\n\n"
        "Выберите способ подбора:"
    )


def help_text() -> str:
    return (
        "**Справка**\n\n"
        "**Команды**\n"
        "/start — главное меню\n"
        "/settings — сменить город\n"
        "/help — эта справка\n\n"
        "**Стандартные сценарии** — кнопки меню и опрос «📝 Опрос».\n"
        "**ИИ-ассистент** — произвольный текст в чат, если запрос не подходит под кнопки "
        "или вы хотите описать пожелания своими словами."
    )
